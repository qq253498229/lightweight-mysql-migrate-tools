SELECT TABLE_CATALOG,
       TABLE_SCHEMA,
       TABLE_NAME,
       TABLE_TYPE,
       ENGINE,
       VERSION,
       ROW_FORMAT,
       TABLE_ROWS,
       AVG_ROW_LENGTH,
       DATA_LENGTH,
       MAX_DATA_LENGTH,
       INDEX_LENGTH,
       DATA_FREE,
       AUTO_INCREMENT,
       CREATE_TIME,
       UPDATE_TIME,
       CHECK_TIME,
       CHARACTER_SET_NAME,
       TABLE_COLLATION,
       CHECKSUM,
       CREATE_OPTIONS,
       TABLE_COMMENT
FROM information_schema.TABLES
         LEFT JOIN information_schema.COLLATION_CHARACTER_SET_APPLICABILITY ON TABLE_COLLATION = COLLATION_NAME
WHERE TABLE_SCHEMA = ? AND TABLE_TYPE = 'BASE TABLE'